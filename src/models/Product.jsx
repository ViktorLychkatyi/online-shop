export class Product {
    constructor(id, title, subtitle, price, discount, starsReview, countReview, image) {
        this.id = id;
        this.title = title;
        this.description = subtitle;
        this.price = price;
        this.discount = discount;
        this.starsReview = starsReview;
        this.countReview = countReview;
        this.image = image;
    }
}
